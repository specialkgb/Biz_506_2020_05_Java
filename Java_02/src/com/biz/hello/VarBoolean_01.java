package com.biz.hello;

public class VarBoolean_01 {

	public static void main(String[] args) {
		
		System.out.println(3 == 3); // true
		System.out.println(3 > 3); // false
		System.out.println(3 < 3); // false
		System.out.println(3 >= 3); // true
		System.out.println(3 != 3); // false
		
		/*
		 * Boolean 대수
		 * 어떤 값 A, B 2개가 각각 True이거나 False인 경우의 값만 있는 경우
		 * 이 값을 Boolean형 이라고 표현을 한다.
		 * =========================================================
		 *   A        or        B          C
		 * ---------------------------------------------------------
		 * False              False      False
		 * False              True       True
		 * True               False      True
		 * True               True       True
		 * =========================================================
		 * 
		 * =========================================================
		 *   A        and        B          C
		 * ---------------------------------------------------------
		 * False               False      False
		 * False               True       False
		 * True                False      False
		 * True                True       True
		 * =========================================================
		 */
		
		
		
	}
}
