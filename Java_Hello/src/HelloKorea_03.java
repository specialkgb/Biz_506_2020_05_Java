
public class HelloKorea_03 {

	/*
	 * 여러줄짜리 주석
	 * 
	 * 여러줄에 걸쳐 주석을 작성하고 할 때
	 * 사용하는 방법
	 * 
	 * 주석을 작성하는 방법
	 * 1. 가급적 문장으로 작성
	 * 2. 나중에 코드를 봤을 때 이해할 수 있도록 작성
	 * 
	 * 숫자 30과 40에 대한 사칙연산을 수행하여
	 * console에 보여주는 코드
	 * 
	 */
	
	
	public static void main(String[] args) {

		System.out.println(30 + 40); // 덧셈연산
		System.out.println(30 - 40); // 뺄셈연산
		System.out.println(30 * 40); // 곱셈연산
		System.out.println(40 / 30); // 나눗셈연산
		
		

	}

}
